package servlet;

import DAO.UsersDAO;
import entity.RoleInformation;
import entity.Users;
import java.io.IOException;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.util.ArrayList;

/**
 *
 * @author tranh
 */
@WebServlet(name = "AddNewUserServlet", urlPatterns = {"/addNewUser"})
public class AddNewUserServlet extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        boolean isSuccess = false;
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String fullname = request.getParameter("fullname");
        String email = request.getParameter("email");
        String roleInStr = request.getParameter("role");
        int role = Integer.parseInt(roleInStr);
        boolean isVerificated = false;
        UsersDAO usersDAO = new UsersDAO();
        isSuccess = usersDAO.addNewUser(username, password, fullname, email, role, isVerificated);
        boolean isAdded = true;
        ArrayList<Users> usersList = usersDAO.loadAllUsers();
        ArrayList<RoleInformation> roleList = usersDAO.loadAllRoleInformationList();
        request.setAttribute("roleList", roleList);
        request.setAttribute("isSuccess", isSuccess);
        request.setAttribute("usersList", usersList);
        request.setAttribute("isAdded", isAdded);
        request.getRequestDispatcher("manager/usersManager.jsp").forward(request, response);
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
