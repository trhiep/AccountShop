    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        Users userAcc = (Users) session.getAttribute("Users");
        String username = userAcc.getUsername();
        String productID = request.getParameter("productID");
        CartDAO cartDAO = new CartDAO();
        String btAction = request.getParameter("btAction");
        String price = request.getParameter("price");
        switch (btAction) {
            case "+":
                cartDAO.addToCart(username, productID, "1", Integer.parseInt(price));
                break;
            case "-":
                cartDAO.decreaseItemInCart(username, productID);
                break;
            case "Delete":
                cartDAO.deleteInCart(username, productID);
                break;
            default:
                throw new AssertionError();
        }
        response.sendRedirect("viewCart");
    }

